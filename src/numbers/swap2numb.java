package numbers;
import java.util.Scanner;

public class swap2numb {

    public static void main(String[] args) {

        int a =5;
        int b =3;
        a = a + b; // a = 8, b =3
        b = a-b; // b=5, a=8
        a = a-b; //a=3, b=5
        System.out.println(a);
        System.out.println(b);
    }
}
